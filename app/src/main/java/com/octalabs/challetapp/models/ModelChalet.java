public class ModelChalet {
    String mChaletName, mLocation, mChaletPrice, mChaletImage;
    Integer mChaletRating;

    public String getmChaletName() {
        return mChaletName;
    }

    public void setmChaletName(String mChaletName) {
        this.mChaletName = mChaletName;
    }

    public String getmLocation() {
        return mLocation;
    }

    public void setmLocation(String mLocation) {
        this.mLocation = mLocation;
    }

    public String getmChaletPrice() {
        return mChaletPrice;
    }

    public void setmChaletPrice(String mChaletPrice) {
        this.mChaletPrice = mChaletPrice;
    }

    public String getmChaletImage() {
        return mChaletImage;
    }

    public void setmChaletImage(String mChaletImage) {
        this.mChaletImage = mChaletImage;
    }

    public Integer getmChaletRating() {
        return mChaletRating;
    }

    public void setmChaletRating(Integer mChaletRating) {
        this.mChaletRating = mChaletRating;
    }
}
